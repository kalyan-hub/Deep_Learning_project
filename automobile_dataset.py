import pandas as pd
import numpy as np

import seaborn as sns
sns.set(color_codes=True)

"""#Read the dataset from the location where it is available"""

auto_df = pd.read_csv("https://gist.githubusercontent.com/lauvshree/20ee07bfaa6d97364006fc2320092526/raw/13a8755aa7d642607c8abc01c091fec5dcb533ca/automobile.csv")

"""#Check the first five records in the data set"""

auto_df.head(5)

"""#Check the type of data"""

auto_df.dtypes

"""#Replace `?` with None. """

auto_df['peak-rpm'] = auto_df['peak-rpm'].replace("?", None)

auto_df['horsepower'] = auto_df['horsepower'].replace("?", None)

auto_df['price'] = auto_df['price'].replace("?", None)

"""#Convert the data type of numeric columns to numeric type"""

auto_df['peak-rpm'] = pd.to_numeric(auto_df['peak-rpm'])

auto_df['horsepower'] = pd.to_numeric(auto_df['horsepower'])

auto_df['price'] = pd.to_numeric(auto_df['price'])

"""#Plot the number of cars in the datasset according to the RPM"""

sns.histplot(auto_df['peak-rpm']);

"""#Plot the engine size and the horsepower in a joint plot and observe"""

sns.jointplot(data=auto_df, x = 'engine-size', y='horsepower')

"""#Plot the engine size and price in a joint plot and observe"""

sns.jointplot(data=auto_df, x = 'engine-size', y='price')

"""#Plot the fueltype and price in a strip plot and observe"""

sns.stripplot(auto_df['fuel-type'], auto_df['price'])

"""#Plot the body-style and the horsepower in a bar plot and observe differntiation based on the engine location"""

sns.barplot(auto_df['body-style'], auto_df['horsepower'], hue=auto_df['engine-location'])

